# Escape Sequences Characters
                              # Escape Sequence Characters are used in strings. These are \n \t \' \" \\

# \n is used to create a new line in the string
#\t is used to create a tab space in the string
#\' this character is used to put one or many words in single line quote 
#\\ is used to create a backslash \.

S = "Rahman Ullah \nloves the \\badminton \n\"sport\""

print(S)
  

A = "Rahman Ullah \nneed a laptop \nbut \the cannot afford the \"laptop\" \n\\byhimself"

print(A)
